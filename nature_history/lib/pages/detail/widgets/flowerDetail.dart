class FlowerDetail {
  final String name;
  final String description;
  final String scientific;
  final String genus;
  final String family;
  final String kingdom;
  final String order;
  final String tribe;
  final String image;

  FlowerDetail({
    required this.name,
    required this.description,
    required this.scientific,
    required this.genus,
    required this.family,
    required this.kingdom,
    required this.order,
    required this.tribe,
    required this.image,
  });
}